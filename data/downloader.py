# ── Events recommended for training signal injection pool ─────────────────────
# High confidence (p_astro>=0.99), not held-out, SNR>=8
TRAINING_INJECTION_EVENTS: list[str] = [
    name for name, gps in GWTC_GPS.items()
    if name not in HELD_OUT_EVENTS
    and GWTC_SNR.get(name, 0) >= 8
    and GWTC_PASTRO.get(name, 0) >= 0.99
]


def fetch_event(
    event_name: str,
    gps_time: float,
    detector: str,
    out_dir: Path,
    duration: float = 64.0,
    sample_rate: int = 4096,
    force: bool = False,
) -> Path | None:
    out_path = out_dir / f"{event_name}_{detector}.hdf5"
    if out_path.exists() and not force:
        log.info(f"  ✓ {out_path.name} cached")
        return out_path
    try:
        from gwpy.timeseries import TimeSeries
    except ImportError:
        log.error("gwpy not installed: pip install gwpy"); sys.exit(1)

    t_start = gps_time - duration / 2
    t_end   = gps_time + duration / 2
    snr     = GWTC_SNR.get(event_name, "?")
    cat     = GWTC_CATALOG.get(event_name, "?")
    log.info(f"  Fetching {event_name} | {cat} | SNR={snr} | {detector} "
             f"[{t_start:.1f}–{t_end:.1f}]")
    try:
        ts = TimeSeries.fetch_open_data(
            detector, t_start, t_end,
            sample_rate=sample_rate, cache=True,
        )
        ts.write(str(out_path), format="hdf5", overwrite=True)
        log.info(f"  → {out_path}")
        return out_path
    except Exception as exc:
        log.warning(f"  ✗ {event_name}/{detector}: {exc}")
        return None


def fetch_noise_segments(
    out_dir: Path,
    detector: str,
    n_segments: int = 200,
    sample_rate: int = 4096,
    segment_duration: float = 64.0,
    force: bool = False,
    observing_run: str = "O3",
) -> list[Path]:
    """
    Fetch off-source noise segments from a known quiet period.
    Avoids any GPS time within 512 s of a known event.
    """
    try:
        from gwpy.timeseries import TimeSeries
    except ImportError:
        log.error("gwpy not installed"); sys.exit(1)

    # GPS ranges for each observing run
    run_ranges = {
        "O1": (1126051217, 1137254417),   # 2015-09-12 to 2016-01-19
        "O2": (1164556817, 1187733618),   # 2016-11-30 to 2017-08-25
        "O3a":(1238166018, 1253977218),   # 2019-04-01 to 2019-10-01
        "O3b":(1256655618, 1269363618),   # 2019-11-01 to 2020-03-27
        "O4": (1369195218, 1421382272),   # 2023-05-24 to 2025-01-18
    }
    run_ranges["O3"] = run_ranges["O3b"]  # default to O3b

    gps_lo, gps_hi = run_ranges.get(observing_run, run_ranges["O4"])
    known_gps = list(GWTC_GPS.values())
    rng       = np.random.default_rng(42)
    candidates = rng.uniform(gps_lo, gps_hi - segment_duration, size=n_segments * 8)

    saved: list[Path] = []
    for t in candidates:
        if len(saved) >= n_segments:
            break
        if any(abs(t - g) < 512 for g in known_gps):
            continue
        tag      = f"noise_{detector}_{int(t)}"
        out_path = out_dir / f"{tag}.hdf5"
        if out_path.exists() and not force:
            saved.append(out_path); continue
        try:
            ts = TimeSeries.fetch_open_data(
                detector, t, t + segment_duration,
                sample_rate=sample_rate, cache=True,
            )
            ts.write(str(out_path), format="hdf5", overwrite=True)
            saved.append(out_path)
            log.info(f"  noise {len(saved)}/{n_segments} → {out_path.name}")
        except Exception as exc:
            log.debug(f"  skip {t:.0f}: {exc}")

    return saved


def select_events(
    min_snr: float = 0.0,
    min_pastro: float = 0.0,
    catalogs: list[str] | None = None,
    exclude: list[str] | None = None,
) -> list[str]:
    """Filter GWTC_GPS by SNR, p_astro, catalog membership."""
    exclude = set(exclude or [])
    result  = []
    for name in GWTC_GPS:
        if name in exclude:
            continue
        if GWTC_SNR.get(name, 0) < min_snr:
            continue
        if GWTC_PASTRO.get(name, 1.0) < min_pastro:
            continue
        if catalogs and GWTC_CATALOG.get(name) not in catalogs:
            continue
        result.append(name)
    return result


def main():
    p = argparse.ArgumentParser(description="Fetch LIGO strain from GWOSC")
    p.add_argument("--events", nargs="*", default=None,
                   help="Event names, or 'held_out', 'training', 'all'")
    p.add_argument("--min-snr",    type=float, default=0.0)
    p.add_argument("--min-pastro", type=float, default=0.0)
    p.add_argument("--catalogs",   nargs="*",  default=None)
    p.add_argument("--detector",   default="L1", choices=["H1","L1","V1"])
    p.add_argument("--duration",   type=float, default=64.0)
    p.add_argument("--sample-rate",type=int,   default=4096)
    p.add_argument("--out-dir",    default="data/strain")
    p.add_argument("--noise-segments", type=int, default=0)
    p.add_argument("--observing-run",  default="O4",
                   choices=["O1","O2","O3","O3a","O3b","O4"])
    p.add_argument("--force", action="store_true")
    args = p.parse_args()

    out_dir = Path(args.out_dir); out_dir.mkdir(parents=True, exist_ok=True)

    # Resolve event list
    if args.events is None or args.events == ["all"]:
        events = select_events(args.min_snr, args.min_pastro, args.catalogs)
    elif args.events == ["held_out"]:
        events = HELD_OUT_EVENTS
    elif args.events == ["training"]:
        events = TRAINING_INJECTION_EVENTS
    else:
        events = args.events

    log.info(f"Fetching {len(events)} events on {args.detector}")
    for name in events:
        if name not in GWTC_GPS:
            log.warning(f"Unknown: {name} — not in GWTC_GPS"); continue
        fetch_event(name, GWTC_GPS[name], args.detector, out_dir,
                    args.duration, args.sample_rate, args.force)

    if args.noise_segments > 0:
        log.info(f"Fetching {args.noise_segments} off-source noise segments "
                 f"from {args.observing_run}...")
        fetch_noise_segments(out_dir, args.detector,
                             n_segments=args.noise_segments,
                             sample_rate=args.sample_rate,
                             observing_run=args.observing_run,
                             force=args.force)

    log.info("Done.")


if __name__ == "__main__":
    main()
